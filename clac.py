def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a/b
    
n1=int(input("Enter the first number:"))
n2=int(input("Enter the second number:"))
print("Addition:",add(n1,n2))
print("Substraction:",sub(n1,n2))
print("Multipication:",mul(n1,n2))
print("Division:",div(n1,n2))
